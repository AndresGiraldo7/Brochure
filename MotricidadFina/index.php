        <h2>¿Qué es la motricidad fina?</h2><br>
        <p>La motricidad fina hace referencia a la coordinación de músculos, huesos y nervios para hacer movimientos más pequeños y precisos.<br><br>
        Algunos especialistas indican que el desarrollo de la motricidad fina inicia a partir de los 2 y 4 meses, cuando tu bebé comienza con la coordinación manos-ojos; mientras que a los 5 meses ya tiene la capacidad de tomar objetos con sus manos.<br><br>
        Alrededor de los 18 meses, la motricidad fina de tu bebé se verá más desarrollada, ya que a esa edad ya toma un plumón y hace sus primeros garabatos, o bien, toma objetos pequeños para colocarlos dentro de cajas o botes.<br><br>
        </p>
