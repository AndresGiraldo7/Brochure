        <h2>¿Qué es la motricidad gruesa?</h2><br>
        <p> Es la capacidad que tenemos para mover los músculos del cuerpo de forma coordinada y a su vez mantener el equilibrio. Con ello, desarrollamos la agilidad, la fuerza y la velocidad necesaria en cada caso.<br><br>
        La motricidad gruesa son también los movimientos amplios de varios grupos musculares como el control de cabeza, la capacidad de sentarnos por sí solos, girar, gatear, mantenerse de pie, caminar, saltar.<br><br>
        A los 3 meses tu bebé va a poder girar la cabeza de un lado a otro, también podrá levantarla. Entre los 3 y 6 meses, comienza a girar su cuerpo; entre los 6 y 9 meses comienza a hacer el intento de sentarse de forma independiente y puede comenzar a gatear. Entre los 12 y 18 meses tu hijo estará listo para dar sus primeros pasos.<br><br>
        </p>
